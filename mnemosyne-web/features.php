<h1>Features</h1>
<br>
These are some of the features of Mnemosyne:
<ul>
<li>Efficient scheduling algorithm, so you don't waste time on things you know well</li>
<li>Support for languages using different scripts through unicode</li>
<li>Support for pictures, sounds and html formatting</li>
<li>Can be integrated with LaTeX to display mathematical formulas</li>
<li>Support for three-sided cards, e.g. foreign words where you are interested in written form, pronunciation and translation
<li>Can be run from a USB key</li>
<li>Can display some basis statistical info on your learning process</li>
<li>Keeps a detailed record of your entire learning process for analysis</li>
<li>Your cards can be organised in categories, which can be activated and disactivated to control your learning process
<li>Clean, deceptively simple user interface, yet fully customisable for advanced users through configuration files and plugins</li>
<li>Available in English, German, Spanish and Dutch</li>
<li>Support for a large number of import and export formats (text, XML, Supermemo, Memaid, ...)</li>
</ul>
