<h1>History</h1>
<br>
The pioneer of flash card software based on spaced repetition is Piotr Wozniak, who released the first version of his <a href="http://www.supermemo.com">SuperMemo</a> program in 1987. A few years later, this program became commercial.
<br><br>
In 2003, SuperMemo inspired David Calinski to write <a href="http://memaid.sf.net">MemAid</a>, since SuperMemo was commercial and did only run under Windows. Peter Bienstman later joined the MemAid team and contributed a SuperKaramba client and a client based on PyQt.
<br><br>
A few years later, David turned MemAid into a commerical product called <a href="http://fullrecall.com/">FullRecall</a>. Peter then used his PyQt client as the basis of a new open source program: the Mnemosyne Project was born.

